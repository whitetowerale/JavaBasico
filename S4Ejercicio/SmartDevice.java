package com.S4Poo.S4Ejercicio;

public class SmartDevice {

    Boolean sdendendido;
    String sdversion;
    double sdtamPantalla;
    double sdcapacidadBat;
    String sdcolorCarcasa;
    String sdso;

    public SmartDevice() {
    }

    public SmartDevice(Boolean sdendendido, String sdversion, double sdtamPantalla, double sdcapacidadBat, String sdcolorCarcasa, String sdso) {
        this.sdendendido = sdendendido;
        this.sdversion = sdversion;
        this.sdtamPantalla = sdtamPantalla;
        this.sdcapacidadBat = sdcapacidadBat;
        this.sdcolorCarcasa = sdcolorCarcasa;
        this.sdso = sdso;
    }
}
